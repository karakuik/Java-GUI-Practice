class Customer{
    String name;
    String address;
    String packages;
    boolean international;
    
    public Customer(String name, String address, String packages, boolean international)
    {
        this.name = name;
        this.address = address;
        this.packages = packages;
        this.international = international;
    }
    
    public String getName(){
        return name;
    }
    
    String getAddress(){
        return address;
    }
    
    String getPackages()
    {
        return packages;
    }
    
    boolean getIntl(){
        return international;
    }
    
    @Override
    public String toString() {
        return "Name: " + name + "\nAddress: " + address + "\nPackages: " + packages
                + "\nInternational: " + international + "\n\n";
    }
}

