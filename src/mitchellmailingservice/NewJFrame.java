    private void LoadFromFileActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_LoadFromFileActionPerformed
        // TODO add your handling code here:
        try{
            FileReader fr = new FileReader("CustomerData.csv");
            Scanner scanner = new Scanner(fr);
            scanner.nextLine();  //consume the heading of the csv file
            while(scanner.hasNextLine()){
                String currentLine = scanner.nextLine();
                String [] customerInfo = currentLine.split(",");
                //Name is in studentInfo[0]
                //International will be in studentInfo[1]
                if(customerInfo[3].equals("1")){
                    arrayList.add(new Customer(customerInfo[0],customerInfo[1],customerInfo[2], true));
                }
                else if(customerInfo[3].equals("0")){
                    arrayList.add(new Customer(customerInfo[0],customerInfo[1],customerInfo[2], false));
                }
            }
        } 
        catch (FileNotFoundException ex) {
            Logger.getLogger(NewJFrame.class.getName()).log(Level.SEVERE, null, ex);
        }

        
    }//GEN-LAST:event_LoadFromFileActionPerformed

